package com.nt.dto;

public class Dto {
	private int workerNo;
	private String workerName;
	private int age;
	private String designation;
	private String workerNominee;
	private int doj;
	private int dor;
	
	
	public int getWorkerNo() {
		return workerNo;
	}
	public void setWorkerNo(int workerNo) {
		this.workerNo = workerNo;
	}
	public String getWorkerName() {
		return workerName;
	}
	public void setWorkerName(String workerName) {
		this.workerName = workerName;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getWorkerNominee() {
		return workerNominee;
	}
	public void setWorkerNominee(String workerNominee) {
		this.workerNominee = workerNominee;
	}
	public int getDoj() {
		return doj;
	}
	public void setDoj(int doj) {
		this.doj = doj;
	}
	public int getDor() {
		return dor;
	}
	public void setDor(int dor) {
		this.dor = dor;
	}

}
